#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	int		i;
	int		k;

	i = 0;
	k = ft_strlen(s1);
	s2 = ((char *)malloc(sizeof(char) * k + 1));
	if (!s2)
	{
		return (s2);
	}
	while (s1[i] != '\0')
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
